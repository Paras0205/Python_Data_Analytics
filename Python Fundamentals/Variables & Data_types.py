# Data types 
# Data types in Python define what kind of value a variable can hold and what operations can be performed on that value.

name = "Paras"        #string
age = 19              #integer
marks = 92.5          #float
male = True           #boolean
subjects = ["Python", "SQL", "Excel"]      # list
coordinates = (10, 20)                     # tuple
unique_nums = {1, 2, 3}                    # set
