#Practice Programs

# Ask the user’s name and greet them.
name = input("Enter your name: ")
print("Hello,", name, "Welcome to the Python programming world!")

# Ask for two numbers and calculate average.
num1 = float(input("Enter the first number: "))
num2 = float(input("Enter the second number: "))
average = (num1 + num2) / 2
print("The average of", num1, "and", num2, "is:", average)

#Create a simple billing calculator.
item1 = float(input("Enter the price of item 1: "))
item2 = float(input("Enter the price of item 2: "))
total = item1 + item2
print("The total bill amount is:", total)